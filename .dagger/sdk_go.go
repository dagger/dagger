// Publish the Go SDK
func (t GoSDK) Publish(
	ctx context.Context,
	tag string,

	// +optional
	dryRun bool,

	// +optional
	// +default="https://github.com/dagger/dagger-go-sdk.git"
	gitRepo string,
	// +optional
	// +default="https://github.com/dagger/dagger.git"
	gitRepoSource string,
	// +optional
	// +default="dagger-ci"
	gitUserName string,
	// +optional
	// +default="hello@dagger.io"
	gitUserEmail string,

	// +optional
	githubToken *dagger.Secret,
	// +optional
	discordWebhook *dagger.Secret,
) error {
	version := strings.TrimPrefix(tag, "sdk/go/")

	if err := gitPublish(ctx, t.Dagger.Git, gitPublishOpts{
		sdk:          "go",
		source:       gitRepoSource,
		sourceTag:    tag,
		sourcePath:   "sdk/go/",
		sourceFilter: "if [ -f go.mod ]; then go mod edit -dropreplace github.com/dagger/dagger; fi",
		sourceEnv:    t.Dagger.Go().Env(),
		dest:         gitRepo,
		destTag:      version,
		username:     gitUserName,
		email:        gitUserEmail,
		githubToken:  githubToken,
		dryRun:       dryRun,
	}); err != nil {
		return err
	}

	if semver.IsValid(version) {
		if err := dag.Releaser().GithubRelease(ctx, gitRepoSource, "sdk/go/"+version, tag, dagger.ReleaserGithubReleaseOpts{
			Notes:  dag.Releaser().ChangeNotes("sdk/go", version),
			Token:  githubToken,
			DryRun: dryRun,
		}); err != nil {
			return err
		}

		if err := dag.Releaser().Notify(ctx, gitRepoSource, "sdk/go/"+version, "üêπ Go SDK", dagger.ReleaserNotifyOpts{
			DiscordWebhook: discordWebhook,
			DryRun:         dryRun,
		}); err != nil {
			return err
		}
	}

	return nil
}

// Bump the Go SDK's Engine dependency
func (t GoSDK) Bump(ctx context.Context, version string) (*dagger.Directory, error) {
	// trim leading v from version
	version = strings.TrimPrefix(version, "v")

	versionFile := fmt.Sprintf(`// Code generated by dagger. DO NOT EDIT.

package engineconn

const CLIVersion = %q
`, version)

	// NOTE: if you change this path, be sure to update .github/workflows/publish.yml so that
	// provision tests run whenever this file changes.
	dir := dag.Directory().WithNewFile("sdk/go/internal/engineconn/version.gen.go", versionFile)
	return dir, nil
}
