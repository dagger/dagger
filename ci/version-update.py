"""
version-update.py

This script is a helper tool to automatically prepare an SDK for release,
primarily for updating each SDKs "cli version" to allow it to handle automatic
provisioning.

- This is fairly simple for the easy releases: python, elixir, etc, which are
  pretty much just prepared tarballs.
- This is incredibly convoluted for the "hard" releases: go and php, whose
  releases are an entire git repository.
"""

import argparse
import os
import subprocess
import textwrap

sdks = {
    "go",
    "python",
    "typescript",
    "elixir",
    "php",
    "rust",
}

filenames = {
    "sdk/go/internal/engineconn/version.gen.go": """\
        // Code generated by dagger. DO NOT EDIT.

        package engineconn

        const CLIVersion = "{}"
    """,
    "sdk/python/src/dagger/_engine/_version.py": """\
	    # Code generated by dagger. DO NOT EDIT.

        CLI_VERSION = "{}"
    """,
    "sdk/typescript/provisioning/default.ts": """\
        // Code generated by dagger. DO NOT EDIT.

        export const CLI_VERSION = "{}"
    """,
    "sdk/elixir/lib/dagger/core/version.ex": """\
        defmodule Dagger.Core.Version do
          @moduledoc false

          @dagger_cli_version "{}"

          def engine_version(), do: @dagger_cli_version
        end
    """,
    "sdk/php/src/Connection/version.php": """\
        <?php /* Code generated by dagger. DO NOT EDIT. */ return '{}';
    """,
    "sdk/rust/crates/dagger-sdk/src/core/version.rs": """\
        pub const DAGGER_ENGINE_VERSION: &'static str = "{}";
    """,
}

excluded_tags = [
    # these sdks were tagged before the engine
    "sdk/go/v0.9.0",
    "sdk/python/v0.9.0",
    "sdk/elixir/v0.9.0",
]


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--sdk", choices=sdks, required=True)
    subparser = parser.add_subparsers(required=True)

    generate_parser = subparser.add_parser("generate")
    generate_parser.add_argument("version")
    generate_parser.set_defaults(func=generate)

    git_parser = subparser.add_parser("git")
    git_parser.add_argument("--ref")
    git_parser.set_defaults(func=git)

    args = parser.parse_args()
    args.func(args)


def generate(args):
    """
    Generate the versioning file for the provided SDK, using the given version.
    """

    _generate(args.sdk, args.version)


def git(args):
    """
    Modify git history on the current branch, ensuring that at each
    sdk/<sdk>/<version> tag, the version file contains the last released engine
    version at that point in time.

    If it doesn't, we generate a new commit that bumps it, and then retag to
    include it, before proceeding to the next sdk/<sdk>/<version> tag in the
    history.
    """

    original_ref = subprocess.run(
        ["git", "rev-parse", "--abbrev-ref", "HEAD"],
        text=True,
        check=True,
        stdout=subprocess.PIPE,
    ).stdout.strip()

    ref = original_ref
    if args.ref:
        ref = args.ref

    tags = [
        tag for tag in _get_tags(ref) if tag.startswith(f"sdk/{args.sdk}/")
    ]

    fnames = {
        fname: content
        for (fname, content) in filenames.items()
        if fname.startswith(f"sdk/{args.sdk}/")
    }

    prev_tag = None
    for tag in tags:
        # NOTE: we might also be able to use changie
        for candidate in reversed(_get_tags(tag)):
            if candidate.startswith("v"):
                engine_tag = candidate
                break
        if engine_tag is None:
            raise f"{tag} has no corresponding engine tag"

        subprocess.run(["git", "checkout", "--quiet", tag], check=True)

        if tag not in excluded_tags:
            generated = _generate(args.sdk, engine_tag)
            if generated:
                subprocess.run(["git", "add", *generated], check=True)

        p = subprocess.run(["git", "diff", "--quiet", "--cached"])
        if p.returncode == 1:
            print(f"[+] updating {tag} version to point to {engine_tag}")

            date = subprocess.run(
                ["git", "show", "--no-patch", "--format=%cI"],
                text=True,
                check=True,
                stdout=subprocess.PIPE,
            ).stdout.strip()

            subprocess.run(
                [
                    "git",
                    "commit",
                    "-s",
                    "-m",
                    f"chore: bump go sdk engine to {engine_tag}",
                ],
                check=True,
                env={
                    # use the timestamps from the past commit (which is how we can
                    # get a reproducible hash)
                    **os.environ,
                    "GIT_AUTHOR_DATE": date,
                    "GIT_COMMITTER_DATE": date,
                },
            )
        else:
            p.check_returncode()

            print(f"[+] skipped {tag} version to point to {engine_tag}")

        if prev_tag:
            subprocess.run(
                [
                    "git",
                    "rebase",
                    prev_tag,
                    "-X",
                    "theirs",
                    "--rebase-merges",
                    "--no-autosquash",
                ],
                check=True,
            )
        subprocess.run(["git", "tag", "-f", tag, "HEAD"], check=True)

        prev_tag = tag

    subprocess.run(["git", "checkout", "--quiet", original_ref], check=True)
    if prev_tag:
        subprocess.run(
            [
                "git",
                "rebase",
                prev_tag,
                "-X",
                "theirs",
                "--rebase-merges",
                "--no-autosquash",
            ],
            check=True,
        )
        prev_tag = None


def _generate(sdk: str, version: str) -> [str]:
    fnames = {
        fname: content
        for (fname, content) in filenames.items()
        if fname.startswith(f"sdk/{sdk}/")
    }

    updated = []
    for fname, content in fnames.items():
        if not os.path.exists(fname):
            continue

        content = textwrap.dedent(content)
        content = content.format(version.lstrip("v"))
        with open(fname, "w") as f:
            f.write(content)

        updated.append(fname)

    return updated


def _get_tags(ref: str) -> [str]:
    p = subprocess.run(
        ["git", "tag", "-l", "--sort=v:refname", "--merged", ref],
        text=True,
        check=True,
        stdout=subprocess.PIPE,
    )
    return p.stdout.strip().splitlines()


if __name__ == "__main__":
    main()
