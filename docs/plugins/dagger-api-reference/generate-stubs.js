#!/usr/bin/env node
// @ts-check
// Regenerates the thin per-type MDX stubs under current_docs/extending/types.
// Each stub just renders <ApiType name="..." />; all content comes from
// docs-graphql/schema.graphqls at build time via the dagger-api-reference
// plugin. The set of stubs is derived from the schema (every core type, with
// the featured ones first), so it stays complete as the schema grows:
//
//   node plugins/dagger-api-reference/generate-stubs.js
//
// docs-dev's References() pipeline invokes it after regenerating the schema.

const fs = require("fs");
const path = require("path");
const featured = require("./coreTypes.js");
const { orderedTypeNames } = require("./schema.js");

const schemaPath = path.resolve(
  __dirname,
  "../../docs-graphql/schema.graphqls"
);
const coreTypes = orderedTypeNames(schemaPath, featured);

// Keep in sync with typeSlug in src/components/api/data.ts.
function typeSlug(name) {
  return name
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

const oldOutDir = path.resolve(__dirname, "../../current_docs/reference/api");
const outDir = path.resolve(__dirname, "../../current_docs/extending/types");
fs.mkdirSync(outDir, { recursive: true });

const banner =
  "{/* Generated by plugins/dagger-api-reference/generate-stubs.js — do not edit.\n" +
  "    Content comes from docs-graphql/schema.graphqls; edit the schema. */}";

function isGeneratedStub(file) {
  try {
    return fs
      .readFileSync(file, "utf8")
      .includes("Generated by plugins/dagger-api-reference/generate-stubs.js");
  } catch {
    return false;
  }
}

function removeGeneratedStubs(dir) {
  if (!fs.existsSync(dir)) return 0;
  let removed = 0;
  for (const f of fs.readdirSync(dir)) {
    if (!f.endsWith(".mdx")) continue;
    const file = path.join(dir, f);
    if (isGeneratedStub(file)) {
      fs.unlinkSync(file);
      removed++;
    }
  }
  return removed;
}

// Drop generated stubs from the old duplicate Reference > API section, and
// clear stale generated stubs in the new canonical Types Reference section
// without touching hand-written type pages.
const removedOld = removeGeneratedStubs(oldOutDir);
const removedNew = removeGeneratedStubs(outDir);

let generated = 0;
let skipped = 0;
coreTypes.forEach((name, index) => {
  const slug = typeSlug(name);
  const file = path.join(outDir, `${slug}.mdx`);
  if (fs.existsSync(file)) {
    skipped++;
    return;
  }
  fs.writeFileSync(
    file,
    `---
title: "${name}"
description: "${name} API reference, generated from the Dagger GraphQL schema."
sidebar_position: ${index + 2}
slug: /extending/types/${slug}
hide_table_of_contents: true
---

${banner}

import ApiType from "@site/src/components/api/ApiType";

<ApiType name="${name}" />
`
  );
  generated++;
});

console.log(
  `Generated ${generated} API reference stubs in ${outDir}; skipped ${skipped} hand-written pages; removed ${removedOld} old stubs and ${removedNew} stale stubs.`
);
