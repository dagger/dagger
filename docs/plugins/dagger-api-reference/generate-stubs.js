#!/usr/bin/env node
// @ts-check
// Regenerates the thin per-type MDX stubs under current_docs/reference/api.
// Each stub just renders <ApiType name="..." />; all content comes from
// docs-graphql/schema.graphqls at build time via the dagger-api-reference
// plugin. Run this when the published core-type list (coreTypes.js) changes:
//
//   node plugins/dagger-api-reference/generate-stubs.js
//
// docs-dev's References() pipeline invokes it after regenerating the schema.

const fs = require("fs");
const path = require("path");
const coreTypes = require("./coreTypes.js");

// Keep in sync with typeSlug in src/components/api/data.ts.
function typeSlug(name) {
  return name
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

const outDir = path.resolve(__dirname, "../../current_docs/reference/api");
fs.mkdirSync(outDir, { recursive: true });

// Drop any stale stubs so a removed core type doesn't linger.
for (const f of fs.readdirSync(outDir)) {
  if (f.endsWith(".mdx")) fs.unlinkSync(path.join(outDir, f));
}

const banner =
  "{/* Generated by plugins/dagger-api-reference/generate-stubs.js — do not edit.\n" +
  "    Content comes from docs-graphql/schema.graphqls; edit the schema. */}";

let position = 2;
for (const name of coreTypes) {
  const slug = typeSlug(name);
  fs.writeFileSync(
    path.join(outDir, `${slug}.mdx`),
    `---
title: "${name}"
description: "${name} API reference, generated from the Dagger GraphQL schema."
sidebar_position: ${position++}
---

${banner}

import ApiType from "@site/src/components/api/ApiType";

<ApiType name="${name}" />
`
  );
}

const links = coreTypes
  .map((n) => `- [${n}](./${typeSlug(n)}.mdx)`)
  .join("\n");
fs.writeFileSync(
  path.join(outDir, "index.mdx"),
  `---
title: "API Reference"
description: "Reference for the Dagger core API types, generated from the GraphQL schema."
sidebar_position: 1
slug: /reference/api
---

${banner}

Reference documentation for the Dagger core API types. Each page is generated
directly from the Dagger GraphQL schema, so signatures and descriptions always
match the running API.

${links}
`
);

console.log(`Generated ${coreTypes.length} API reference stubs in ${outDir}`);
