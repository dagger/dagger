"""Unit tests for the static entrypoint renderer (pure, no engine)."""

from __future__ import annotations

import logging

from dagger.mod import Module
from dagger.mod._introspect.entrypoint import render_entrypoint


def _build_module() -> Module:
    mod = Module()

    @mod.object_type
    class Test:
        @mod.function
        def at_level(self, level: int = logging.INFO) -> int:
            return level

    return mod


def test_rendered_entrypoint_compiles():
    src = render_entrypoint(
        _build_module(), main_object_name="Test", module_name="test"
    )
    # Must be valid Python.
    compile(src, "_dagger_main.py", "exec")
    # Generated marker + def-phase wiring present.
    assert "Generated by" in src
    assert "typedefs_from_json" in src


def test_rendered_entrypoint_embeds_resolved_values():
    src = render_entrypoint(
        _build_module(), main_object_name="Test", module_name="test"
    )

    ns: dict = {}
    exec(src, ns)

    assert ns["MAIN_OBJECT"] == "Test"
    assert ns["MODULE_NAME"] == "test"

    types = {t["name"] for t in ns["MODULE_TYPES"]["__schema"]["types"]}
    assert {"Test", "Query"} <= types

    # The runtime-resolved default (#13234) is baked into the static artifact.
    # Names stay native (snake_case) so the def-phase replay matches the invoke
    # keying (normalize_name) rather than the GraphQL API's camelCase.
    test = next(
        t for t in ns["MODULE_TYPES"]["__schema"]["types"] if t["name"] == "Test"
    )
    level = next(
        a
        for f in test["fields"]
        if f["name"] == "at_level"
        for a in f["args"]
        if a["name"] == "level"
    )
    assert level["defaultValue"] == "20"
