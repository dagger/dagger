func (cli *CliDev) releaseChecksumMap(ctx context.Context, dist *dagger.Directory) (map[string]string, error) {
	contents, err := dist.File("checksums.txt").Contents(ctx)
	if err != nil {
		return nil, err
	}

	checksums := map[string]string{}
	for _, line := range strings.Split(contents, "\n") {
		fields := strings.Fields(line)
		if len(fields) == 0 {
			continue
		}
		if len(fields) != 2 {
			return nil, fmt.Errorf("invalid checksum line: %q", line)
		}
		checksums[fields[1]] = fields[0]
	}
	return checksums, nil
}

func (cli *CliDev) releaseNixArchives(ctx context.Context, dist *dagger.Directory, tag string, checksums map[string]string) ([]cliNixArchive, error) {
	linuxAMD64, err := requireReleaseArchiveName(tag, "linux_amd64.tar.gz", checksums)
	if err != nil {
		return nil, err
	}
	linuxARMv7, err := requireReleaseArchiveName(tag, "linux_armv7.tar.gz", checksums)
	if err != nil {
		return nil, err
	}
	linuxARM64, err := requireReleaseArchiveName(tag, "linux_arm64.tar.gz", checksums)
	if err != nil {
		return nil, err
	}
	darwinAMD64, err := requireReleaseArchiveName(tag, "darwin_amd64.tar.gz", checksums)
	if err != nil {
		return nil, err
	}
	darwinARM64, err := requireReleaseArchiveName(tag, "darwin_arm64.tar.gz", checksums)
	if err != nil {
		return nil, err
	}

	archives := []cliNixArchive{
		{Platform: "x86_64-linux", Name: linuxAMD64},
		{Platform: "armv7l-linux", Name: linuxARMv7},
		{Platform: "aarch64-linux", Name: linuxARM64},
		{Platform: "x86_64-darwin", Name: darwinAMD64},
		{Platform: "aarch64-darwin", Name: darwinARM64},
	}

	names := make([]string, 0, len(archives))
	for _, archive := range archives {
		names = append(names, archive.Name)
	}
	hashes, err := cli.nixHashes(ctx, dist, names)
	if err != nil {
		return nil, err
	}
	for i, archive := range archives {
		hash, ok := hashes[archive.Name]
		if !ok {
			return nil, fmt.Errorf("missing nix hash for %s", archive.Name)
		}
		archives[i].Hash = hash
	}
	return archives, nil
}

func (cli *CliDev) nixHashes(ctx context.Context, dist *dagger.Directory, archives []string) (map[string]string, error) {
	ctr := dag.
		Alpine(dagger.AlpineOpts{
			Branch:   "3.22",
			Packages: []string{"ca-certificates", "coreutils", "curl", "xz"},
		}).
		Container().
		WithDirectory("/nix", dag.Directory()).
		WithNewFile("/etc/nix/nix.conf", `build-users-group =`).
		WithExec([]string{"sh", "-ec", "curl -fsSL https://nixos.org/nix/install | sh -s -- --no-daemon"})
	path, err := ctr.EnvVariable(ctx, "PATH")
	if err != nil {
		return nil, err
	}

	out, err := ctr.
		WithEnvVariable("PATH", path+":/nix/var/nix/profiles/default/bin").
		WithExec([]string{"nix-hash", "--version"}).
		WithMountedDirectory("/dist", dist).
		WithNewFile("/archives", strings.Join(archives, "\n")+"\n").
		WithExec([]string{"sh", "-ec", `set -eu
while IFS= read -r archive; do
	[ -n "$archive" ] || continue
	printf '%s\t' "$archive"
	nix-hash --type sha256 --flat --base32 "/dist/$archive"
done < /archives`}).
		Stdout(ctx)
	if err != nil {
		return nil, err
	}

	hashes := map[string]string{}
	for _, line := range strings.Split(out, "\n") {
		if strings.TrimSpace(line) == "" {
			continue
		}
		parts := strings.SplitN(line, "\t", 2)
		if len(parts) != 2 {
			return nil, fmt.Errorf("invalid nix hash line: %q", line)
		}
		hashes[parts[0]] = strings.TrimSpace(parts[1])
	}
	return hashes, nil
}

func releaseArchiveName(tag string, suffix string) string {
	return "dagger_" + tag + "_" + suffix
}

func requireReleaseArchiveName(tag string, suffix string, checksums map[string]string) (string, error) {
	name := releaseArchiveName(tag, suffix)
	if _, ok := checksums[name]; !ok {
		return "", fmt.Errorf("missing checksum for %s", name)
	}
	return name, nil
}

