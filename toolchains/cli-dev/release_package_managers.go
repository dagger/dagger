func nixPackage(version string, baseURL string, archives []cliNixArchive) string {
	var shaLines strings.Builder
	var urlLines strings.Builder
	var platformLines strings.Builder
	for _, archive := range archives {
		shaLines.WriteString(fmt.Sprintf("    %s = %q;\n", archive.Platform, archive.Hash))
		urlLines.WriteString(fmt.Sprintf("    %s = %q;\n", archive.Platform, baseURL+"/"+archive.Name))
		platformLines.WriteString(fmt.Sprintf("      %q\n", archive.Platform))
	}

	return fmt.Sprintf(`# %[1]s
# vim: set ft=nix ts=2 sw=2 sts=2 et sta
{
system ? builtins.currentSystem
, lib
, fetchurl
, installShellFiles
, stdenvNoCC
}:
let
  shaMap = {
%[3]s  };

  urlMap = {
%[4]s  };
in
stdenvNoCC.mkDerivation {
  pname = "dagger";
  version = "%[2]s";
  src = fetchurl {
    url = urlMap.${system};
    sha256 = shaMap.${system};
  };

  sourceRoot = ".";

  nativeBuildInputs = [ installShellFiles ];

  installPhase = ''
    install -Dm755 dagger $out/bin/dagger
  '';

  postInstall = ''
    installShellCompletion --cmd dagger \
      --bash <($out/bin/dagger completion bash) \
      --fish <($out/bin/dagger completion fish) \
      --zsh <($out/bin/dagger completion zsh)
  '';

  system = system;

  meta = {
    description = "Dagger is an integrated platform to orchestrate the delivery of applications";
    homepage = "https://dagger.io";
    license = lib.licenses.asl20;

    sourceProvenance = [ lib.sourceTypes.binaryNativeCode ];

    platforms = [
%[5]s    ];
  };
}
`, releaseGeneratedComment, version, shaLines.String(), urlLines.String(), platformLines.String())
}

