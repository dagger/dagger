// Change the required dagger engine version across all components
func (r *Release) Bump(
	ctx context.Context,
	// The new required engine version
	engineVersion string,
) (*dagger.Changeset, error) {
	var (
		bumpDocs, bumpHelm, bumpSDKs *dagger.Changeset
	)
	err := parallel.New().
		WithJob("bump docs version", func(ctx context.Context) error {
			var err error
			bumpDocs, err = r.bumpDocsVersion(engineVersion).Sync(ctx)
			return err
		}).
		WithJob("bump helm chart version", func(ctx context.Context) error {
			var err error
			bumpHelm, err = r.bumpHelmVersion(ctx, engineVersion)
			return err
		}).
		WithJob("bump SDK versions", func(ctx context.Context) error {
			var err error
			bumpSDKs, err = r.bumpSDKVersions(ctx, engineVersion)
			return err
		}).
		Run(ctx)
	if err != nil {
		return nil, err
	}
	return changesetMerge(bumpSDKs, bumpDocs, bumpHelm), nil
}

func (r *Release) bumpDocsVersion(engineVersion string) *dagger.Changeset {
	version := strings.TrimPrefix(engineVersion, "v")
	return r.generatedFileChanges(
		"docs/current_docs/partials/version.js",
		fmt.Sprintf(`// Code generated by dagger. DO NOT EDIT.

export const daggerVersion = "%s";
`, version),
	)
}

func (r *Release) bumpHelmVersion(ctx context.Context, engineVersion string) (*dagger.Changeset, error) {
	// Release mutates Chart.yaml directly because helm-dev is only a
	// chart-checking toolchain now, not a release dependency.
	chartYaml, err := r.helmSetVersion(ctx, engineVersion)
	if err != nil {
		return nil, err
	}
	before := r.releaseSource("helm/dagger/Chart.yaml")
	return before.
		WithFile("helm/dagger/Chart.yaml", chartYaml).
		Changes(before).
		Sync(ctx)
}

func (r *Release) bumpSDKVersions(ctx context.Context, engineVersion string) (*dagger.Changeset, error) {
	var (
		bumpGo, bumpPython, bumpTypescript, bumpPHP *dagger.Changeset
		bumpElixir, bumpRust, bumpJava              *dagger.Changeset
	)
	err := parallel.New().
		WithJob("bump go sdk version", func(ctx context.Context) error {
			bumpGo = r.bumpGoSDKVersion(engineVersion)
			return nil
		}).
		WithJob("bump python sdk version", func(ctx context.Context) error {
			bumpPython = r.bumpPythonSDKVersion(engineVersion)
			return nil
		}).
		WithJob("bump typescript sdk version", func(ctx context.Context) error {
			bumpTypescript = r.bumpTypescriptSDKVersion(engineVersion)
			return nil
		}).
		WithJob("bump php sdk version", func(ctx context.Context) error {
			bumpPHP = r.bumpPHPSDKVersion(engineVersion)
			return nil
		}).
		WithJob("bump elixir sdk version", func(ctx context.Context) error {
			bumpElixir = r.bumpElixirSDKVersion(engineVersion)
			return nil
		}).
		WithJob("bump rust sdk version", func(ctx context.Context) error {
			var err error
			bumpRust, err = r.bumpRustSDKVersion(ctx, engineVersion)
			return err
		}).
		WithJob("bump java sdk version", func(ctx context.Context) error {
			var err error
			bumpJava, err = r.bumpJavaSDKVersion(ctx, engineVersion)
			return err
		}).
		Run(ctx)
	if err != nil {
		return nil, err
	}
	return changesetMerge(
		bumpGo,
		bumpPython,
		bumpTypescript,
		bumpPHP,
		bumpElixir,
		bumpRust,
		bumpJava,
	), nil
}

