func (r *Release) bumpRustSDKVersion(ctx context.Context, engineVersion string) (*dagger.Changeset, error) {
	version := strings.TrimPrefix(engineVersion, "v")

	versionContents, err := r.releaseSource(rustSDKVersionFile).File(rustSDKVersionFile).Contents(ctx)
	if err != nil {
		return nil, err
	}

	versionRe, err := regexp.Compile(`pub const DAGGER_ENGINE_VERSION: &str = "([0-9\.-a-zA-Z]+)";`)
	if err != nil {
		return nil, err
	}

	versionBumpedContents := versionRe.ReplaceAllString(
		versionContents,
		fmt.Sprintf(`pub const DAGGER_ENGINE_VERSION: &str = "%s";`, version),
	)

	before := r.releaseSource(
		"sdk/rust/crates",
		rustCargoTomlFile,
		rustCargoLockFile,
	)
	base := dag.Container().
		From(rustSDKImage+"@"+rustSDKImageDigest).
		WithEnvVariable("CARGO_HOME", "/root/.cargo").
		WithMountedCache("/root/.cargo", dag.CacheVolume("rust-cargo-"+rustSDKImage)).
		WithMountedDirectory("/src", before).
		WithWorkdir("/src/sdk/rust").
		WithExec([]string{"cargo", "install", "cargo-edit@" + rustCargoEditVersion, "--locked"}).
		WithExec([]string{"cargo", "set-version", "-p", rustSDKCrate, version})

	return before.
		WithNewFile(rustSDKVersionFile, versionBumpedContents).
		WithFile(rustCargoTomlFile, base.File("Cargo.toml")).
		WithFile(rustCargoLockFile, base.File("Cargo.lock")).
		Changes(before).
		Sync(ctx)
}

func (r *Release) bumpJavaSDKVersion(ctx context.Context, engineVersion string) (*dagger.Changeset, error) {
	version := strings.TrimPrefix(engineVersion, "v")
	poms := []string{
		javaSDKSourcePath + "/dagger-codegen-maven-plugin/pom.xml",
		javaSDKSourcePath + "/dagger-java-annotation-processor/pom.xml",
		javaSDKSourcePath + "/dagger-java-sdk/pom.xml",
		javaSDKSourcePath + "/dagger-java-samples/pom.xml",
		javaSDKSourcePath + "/pom.xml",
		javaSDKSourcePath + "/runtime/template/pom.xml",
	}
	before := r.releaseSource(
		javaSDKSourcePath+"/dagger-codegen-maven-plugin/",
		javaSDKSourcePath+"/dagger-java-annotation-processor/",
		javaSDKSourcePath+"/dagger-java-sdk/",
		javaSDKSourcePath+"/dagger-java-samples/pom.xml",
		javaSDKSourcePath+"/runtime/template/",
		javaSDKSourcePath+"/runtime/images/",
		javaSDKSourcePath+"/LICENSE",
		javaSDKSourcePath+"/README.md",
		javaSDKSourcePath+"/pom.xml",
	)
	mavenImage := r.releaseSource(javaRuntimeMavenImage).
		Directory(javaRuntimeMavenImage).
		DockerBuild()
	bumpCtr := mavenImage.
		WithDirectory(javaWorkspacePath, before).
		WithWorkdir(javaWorkspacePath + "/" + javaSDKSourcePath).
		WithExec([]string{
			"mvn",
			"versions:set",
			"-DgenerateBackupPoms=false",
			"-DnewVersion=" + version,
		}).
		WithExec([]string{
			"mvn",
			"versions:set-property",
			"-DgenerateBackupPoms=false",
			"-Dproperty=daggerengine.version",
			"-DnewVersion=" + version,
		}).
		WithWorkdir(javaWorkspacePath + "/" + javaSDKSourcePath + "/runtime/template").
		WithExec([]string{
			"mvn",
			"versions:set-property",
			"-DgenerateBackupPoms=false",
			"-Dproperty=dagger.module.deps",
			"-DnewVersion=" + version + "-template-module",
		})

	after := before
	for _, pom := range poms {
		after = after.WithFile(pom, bumpCtr.File(javaWorkspacePath+"/"+pom))
	}
	return after.Changes(before).Sync(ctx)
}

func (r *Release) generatedFileChanges(path, contents string) *dagger.Changeset {
	before := r.releaseSource(path)
	return before.WithNewFile(path, contents).Changes(before)
}

func (r *Release) releaseSource(include ...string) *dagger.Directory {
	return dag.CurrentWorkspace().Directory("/", dagger.WorkspaceDirectoryOpts{
		Include: include,
	})
}

// Merge Changesets together
// FIXME: move this to core dagger: https://github.com/dagger/dagger/issues/11189
func changesetMerge(changesets ...*dagger.Changeset) *dagger.Changeset {
	before := dag.Directory()
	for _, changeset := range changesets {
		before = before.WithDirectory("", changeset.Before())
	}
	after := before
	for _, changeset := range changesets {
		after = after.WithChanges(changeset)
	}
	return after.Changes(before)
}
