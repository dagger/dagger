// Regenerate Rust SDK files that reference the target Dagger Engine version.
// +generate
func (r *Release) RustSdkTargetVersion(ctx context.Context) (*dagger.Changeset, error) {
	const (
		image         = "rust:1.77-bookworm"
		imageDigest   = "sha256:83101f6985c93e1e6501b3375de188ee3d2cbb89968bcc91611591f9f447bd42"
		versionFile   = "sdk/rust/crates/dagger-sdk/src/core/version.rs"
		cargoTomlFile = "sdk/rust/Cargo.toml"
		cargoLockFile = "sdk/rust/Cargo.lock"
	)

	version := r.targetVersionWithoutV()

	versionContents, err := r.releaseSource(versionFile).File(versionFile).Contents(ctx)
	if err != nil {
		return nil, err
	}

	versionRe := regexp.MustCompile(`pub const DAGGER_ENGINE_VERSION: &str = "([0-9\.-a-zA-Z]+)";`)
	versionBumpedContents := versionRe.ReplaceAllString(
		versionContents,
		fmt.Sprintf(`pub const DAGGER_ENGINE_VERSION: &str = "%s";`, version),
	)

	before := r.releaseSource(
		"sdk/rust/crates",
		cargoTomlFile,
		cargoLockFile,
	)
	base := dag.Container().
		From(image+"@"+imageDigest).
		WithEnvVariable("CARGO_HOME", "/root/.cargo").
		WithMountedCache("/root/.cargo", dag.CacheVolume("rust-cargo-"+image)).
		WithMountedDirectory("/src", before).
		WithWorkdir("/src/sdk/rust").
		WithExec([]string{"cargo", "install", "cargo-edit@0.13.0", "--locked"}).
		WithExec([]string{"cargo", "set-version", "-p", "dagger-sdk", version})

	return before.
		WithNewFile(versionFile, versionBumpedContents).
		WithFile(cargoTomlFile, base.File("Cargo.toml")).
		WithFile(cargoLockFile, base.File("Cargo.lock")).
		Changes(before).
		Sync(ctx)
}

// Regenerate Java SDK files that reference the target Dagger Engine version.
// +generate
func (r *Release) JavaSdkTargetVersion(ctx context.Context) (*dagger.Changeset, error) {
	const (
		sourcePath        = "sdk/java"
		workspacePath     = "/src"
		runtimeMavenImage = "sdk/java/runtime/images/maven"
	)

	version := r.targetVersionWithoutV()
	poms := []string{
		sourcePath + "/dagger-codegen-maven-plugin/pom.xml",
		sourcePath + "/dagger-java-annotation-processor/pom.xml",
		sourcePath + "/dagger-java-sdk/pom.xml",
		sourcePath + "/dagger-java-samples/pom.xml",
		sourcePath + "/pom.xml",
		sourcePath + "/runtime/template/pom.xml",
	}
	before := r.releaseSource(
		sourcePath+"/dagger-codegen-maven-plugin/",
		sourcePath+"/dagger-java-annotation-processor/",
		sourcePath+"/dagger-java-sdk/",
		sourcePath+"/dagger-java-samples/pom.xml",
		sourcePath+"/runtime/template/",
		sourcePath+"/runtime/images/",
		sourcePath+"/LICENSE",
		sourcePath+"/README.md",
		sourcePath+"/pom.xml",
	)
	mavenImage := r.releaseSource(runtimeMavenImage).
		Directory(runtimeMavenImage).
		DockerBuild()
	bumpCtr := mavenImage.
		WithDirectory(workspacePath, before).
		WithWorkdir(workspacePath + "/" + sourcePath).
		WithExec([]string{
			"mvn",
			"versions:set",
			"-DgenerateBackupPoms=false",
			"-DnewVersion=" + version,
		}).
		WithExec([]string{
			"mvn",
			"versions:set-property",
			"-DgenerateBackupPoms=false",
			"-Dproperty=daggerengine.version",
			"-DnewVersion=" + version,
		}).
		WithWorkdir(workspacePath + "/" + sourcePath + "/runtime/template").
		WithExec([]string{
			"mvn",
			"versions:set-property",
			"-DgenerateBackupPoms=false",
			"-Dproperty=dagger.module.deps",
			"-DnewVersion=" + version + "-template-module",
		})

	after := before
	for _, pom := range poms {
		after = after.WithFile(pom, bumpCtr.File(workspacePath+"/"+pom))
	}
	return after.Changes(before).Sync(ctx)
}

func (r *Release) generatedFileChanges(path, contents string) *dagger.Changeset {
	before := r.releaseSource(path)
	return before.WithNewFile(path, contents).Changes(before)
}

func (r *Release) targetVersionWithV() string {
	return r.TargetVersion
}

func (r *Release) targetVersionWithoutV() string {
	return strings.TrimPrefix(r.TargetVersion, "v")
}

func (r *Release) releaseSource(include ...string) *dagger.Directory {
	return dag.CurrentWorkspace().Directory("/", dagger.WorkspaceDirectoryOpts{
		Include: include,
	})
}
