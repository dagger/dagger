// Regenerate docs files that reference the target Dagger Engine version.
// +generate
func (r *Release) DocsTargetVersion() *dagger.Changeset {
	version := r.targetVersionWithoutV()
	return r.generatedFileChanges(
		"docs/current_docs/partials/version.js",
		fmt.Sprintf(`// Code generated by dagger. DO NOT EDIT.

export const daggerVersion = "%s";
`, version),
	)
}

// Regenerate Helm chart files that reference the target Dagger Engine version.
// +generate
func (r *Release) HelmTargetVersion(ctx context.Context) (*dagger.Changeset, error) {
	// Release mutates Chart.yaml directly because helm-dev is only a
	// chart-checking toolchain now, not a release dependency.
	chartYaml, err := r.helmSetVersion(ctx, r.targetVersionWithV())
	if err != nil {
		return nil, err
	}
	before := r.releaseSource("helm/dagger/Chart.yaml")
	return before.
		WithFile("helm/dagger/Chart.yaml", chartYaml).
		Changes(before).
		Sync(ctx)
}

